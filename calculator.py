def add(a,b):
    answer = a + b
    return answer

def subtract(a,b):
    answer = a - b
    return answer

def multiply(a,b):
    answer = a * b
    return answer

def devide(a,b):
    answer = a / b
    return answer

def modulus(a,b):
    answer = a % b
    return answer

def exponent(a,b):
    answer = a**b
    return answer

def int_devide(a,b):
    answer = a // b
    return answer

def square(a):
    answer = a**2
    return answer

def cube(a):
    answer = a**3
    return answer

def fuctorial(number):
    factor = 1
    for x in range(1,number + 1):
        factor *= 1
    return factor
